package edu.stanford.smi.protege.ui;

import java.util.*;
import edu.stanford.smi.protege.model.*;
import edu.stanford.smi.protege.util.*;

/**
 *  Description of the class
 *
 * @author    Ray Fergerson <fergerson@smi.stanford.edu>
 */
public class FrameNameComparator implements Comparator {

    public int compare(Object o1, Object o2) {
        int result;
        if (o1 instanceof Instance && o2 instanceof Instance) {
            String t1 = ((Instance) o1).getName();
            String t2 = ((Instance) o2).getName();
            result = t1.compareToIgnoreCase(t2);
        } else {
            Log.error("Invalid types", this, "compare", o1, o2);
            result = 0;
        }
        return result;
    }
}
